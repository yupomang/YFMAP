package com.yondervision.yfmap.result.zunyi;

public class AppApi00801Result01 {
	   
		/** X坐标 */					
		private String x = "";					
		/** Y坐标 */					
		private String y = "";					
		/** 图片地址 */					
		private String img = "";					
							
		/**					
		 *<pre> 执行获取X坐标处理 </pre>					
		 * @return x X坐标					
		 */					
		public String getX() {					
		    return x;					
		}					
							
		/**					
		 *<pre> 执行设定X坐标处理 </pre>					
		 * @param x X坐标					
		 */					
		public void setX(String x) {					
		    this.x = x;					
		}					
							
		/**					
		 *<pre> 执行获取Y坐标处理 </pre>					
		 * @return y Y坐标					
		 */					
		public String getY() {					
		    return y;					
		}					
							
		/**					
		 *<pre> 执行设定Y坐标处理 </pre>					
		 * @param y Y坐标					
		 */					
		public void setY(String y) {					
		    this.y = y;					
		}					
							
		/**					
		 *<pre> 执行获取图片地址处理 </pre>					
		 * @return img 图片地址					
		 */					
		public String getImg() {					
		    return img;					
		}					
							
		/**					
		 *<pre> 执行设定图片地址处理 </pre>					
		 * @param img 图片地址					
		 */					
		public void setImg(String img) {					
		    this.img = img;					
		}	
	
}
